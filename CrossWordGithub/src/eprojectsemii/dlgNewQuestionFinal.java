    private void cbSizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSizeActionPerformed
        id = q.createID(cbDifficult.getSelectedIndex(), cbSize.getSelectedIndex());
        txtQuestionID.setText(id);
        pnq = new pnNewQuestion();
        pnMain.removeAll();
        pnMain.repaint();
        switch (cbSize.getSelectedIndex()) {
            case 0:
                size = 4;
                pnq.setSizexy(4);
                pnMain.setSize(200, 200);
                break;
            case 1:
                size =6;
                pnq.setSizexy(6);
                pnMain.setSize(300, 300);
                break;
            case 2:
                size = 8;
                pnq.setSizexy(8);
                pnMain.setSize(400, 400);
                break;
        }
        
        pnq.Draw();
        pnMain.add(pnq);
        pnq.setVisible(true);
        pack();
    }//GEN-LAST:event_cbSizeActionPerformed
    
